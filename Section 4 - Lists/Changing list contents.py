# Purpose: A program to demonstrate how to change the contents of a list

fruits = ['Strawberry', 'Lemon', 'Orange', 'Raspberry', 'Cherry']
print(fruits)

fruits[0] = 'Apple'
fruit = 'Melon'
fruits[1] = fruit
fruits[2] = 'Raspberry'
fruits[3] = fruits[4]
fruits[4] = 'Pineapple'

print(fruits)
