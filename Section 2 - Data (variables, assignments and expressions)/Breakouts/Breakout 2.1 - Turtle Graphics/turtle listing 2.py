# Purpose: A program to demonstrate the use of variables with turtle graphics

from turtle import *

hideturtle() # hide the turtle
color("red") # set the pen colour to red

lineLength = 50
forward(lineLength)
left(90)
forward(lineLength + 50)
left(90)
forward(lineLength + 50)
left(90)
forward(lineLength + 50)

