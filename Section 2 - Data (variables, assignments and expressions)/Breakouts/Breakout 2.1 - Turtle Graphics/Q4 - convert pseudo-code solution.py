from turtle import *

pensize(2)

angle = 30          # Initialise a variable called angle to 30
forward(50)     # Move forward by 50 units
left(angle)     # Turn left by angle degrees
angle = angle + 10  # Increase the angle by 10 degrees
forward(50)     # Move forward by 50 units
right(angle)    # Turn right by angle degrees
angle = angle + 10  # Increase the angle by 10 degrees
forward(50)     # Move forward by 50 units
left(angle)     # Turn left by angle degrees
angle = angle + 10  # Increase the angle by 10 degrees
forward(50)     # Move forward by 50 units
