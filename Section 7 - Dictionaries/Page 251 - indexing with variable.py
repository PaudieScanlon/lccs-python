# Purpose: A program to demonstrate dictionary indexing - use of variables as keys

d = {
    "OMG" : "Oh My God!",
    "LOL" : "Laugh out Loud",
    "IMHO" : "In My Humble Opinion",
}

key = "OMG"
value = d[key]
print(value) # displays Oh My God!


