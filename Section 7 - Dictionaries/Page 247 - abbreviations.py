# Purpose: A program to demonstrate the creation of dictionaries

abbr1 = dict( {
    "OMG" : "Oh My God!",
    "LOL" : "Laugh out Loud",
    "IMHO" : "In My Humble Opinion",
})

abbr2 = dict(
    OMG = "Oh My God!",
    LOL = "Laugh out Loud",
    IMHO = "In My Humble Opinion",
)

print(abbr1)
print(abbr2)

